package com.fetchProject.demo.model;

import java.util.List;
// receipt detail
public class Receipt {
    private String retailer;         // Retailer's name
    private String purchaseDate;     // Date of purchase
    private String purchaseTime;     // Time of purchase
    private List<Item> items;        // List of items on the receipt
    private String total;           // total amount of receipt

    public String getRetailer() {
        return retailer;
    }

    public void setRetailer(String retailer) {
        this.retailer = retailer;
    }

    public String getPurchaseDate() {
        return purchaseDate;
    }

    public void setPurchaseDate(String purchaseDate) {
        this.purchaseDate = purchaseDate;
    }

    public String getPurchaseTime() {
        return purchaseTime;
    }

    public void setPurchaseTime(String purchaseTime) {
        this.purchaseTime = purchaseTime;
    }

    public List<Item> getItems() {
        return items;
    }

    public void setItems(List<Item> items) {
        this.items = items;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }
}
